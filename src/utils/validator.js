/* -------------------------------------------------------------------------- */
// 이메일 체크 유틸리티
// 이메일 조건: @ 포함
/* -------------------------------------------------------------------------- */

export const isEmail = (value) => {
  // const regExp =
  // /^[0-9a-zA-Z]([-_\.]?[0-9a-zA-Z])*@[0-9a-zA-Z]([-_\.]?[0-9a-zA-Z])*\.[a-zA-Z]{2,3}$/i;

  return value.indexOf("@") !== -1;
};

/* -------------------------------------------------------------------------- */
// 패스워드 체크 유틸리티
// 비밀번호 조건: 8자 이상
/* -------------------------------------------------------------------------- */

export const isPassword = (value) => {
  return value.length >= 8;
};

/* -------------------------------------------------------------------------- */
// validator
/* -------------------------------------------------------------------------- */

export const validator = {
  isEmail,
  isPassword,
};
